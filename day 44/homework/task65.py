def compare(value1, value2, operator):
    if operator == "==":
        value1 == value2
    elif operator == ">":
        value1 > value2
    elif operator == "<":
        value1 < value2
    elif operator == ">=":
        value1 >= value2
    elif operator == "<=":
        value1 <= value2
    elif operator == "!=":
        value1 != value2